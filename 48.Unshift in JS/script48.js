//Unshift()
//Add First Element at Start

student = ["Dev", "Sathish", "Hari", "Vijay", ""];
console.log("Before Unshift : " + student);

let len = student.unshift("Vasavi");
console.log("Length : " + len);
console.log("After Unshift : " + student);

//Mulitiple Values
len = student.unshift("Navya", "Sri");
console.log("Length : " + len);
console.log("After Unshift : " + student);
