// Shift()

let student = ["Vasavi", "Dev", "Sri", "Hari", "Sathish", "Vijay"];
console.log("Before Shift : " + student);

let element = student.shift(); // removes the first element
console.log("After Shift : " + student);
console.log("Removed Element : " + element);

console.log("Before Shift : " + student);
element = student.shift(); // removes the next first element
console.log("After Shift : " + student);
console.log("Removed Element : " + element);
