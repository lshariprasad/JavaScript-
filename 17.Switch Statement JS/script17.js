// Switch Statement //

// Example 1 //

let number = Number(prompt("Enter The Number : "));

switch (number) {
  case 1:
    console.log("One");
    break;
  case 2:
    console.log("Two");
    break;
  case 3:
    console.log("Three");
    break;
  default:
    console.log("Invalid Input");
    break;
}
