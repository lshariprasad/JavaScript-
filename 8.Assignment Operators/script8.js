// || Assignment Operators in JS || //

let a = 10;
let b = 5;

a += b; // a = a + b
console.log("a += b: ", a);

a -= b; // a = a - b
console.log("a -=b:", a);

a *= b; // a = a*b;
console.log("a *=b:", a);

a /= b; // a = a/b;
console.log("a /=b:", a);

a %= b; // a = a%b;
console.log("a %=b:", a);

a **= b; // a = a**b;
console.log("a**b:", a);
