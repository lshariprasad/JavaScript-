// Increment (++) Operator or Decrement (--) Operator

let a = 1;
a++;
console.log(a); // 2

let b = 1;
b--;
console.log(b); // 0

let c = 5;
++c;
console.log(c); // 6

let d = 5;
--d;
console.log(d); // 4

let e = 10;
console.log(e++); // 10 (prints e, then increments)
console.log(e); // 11 (e has been incremented)
